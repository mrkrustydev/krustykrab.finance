import BigNumber from 'bignumber.js'
import { ethers } from 'ethers'

export const DefaultEspressoPoolId = 0

export const approve = async (lpContract, mrKrabsContract, account) => {
  return lpContract.methods
    .approve(mrKrabsContract.options.address, ethers.constants.MaxUint256)
    .send({ from: account })
}

export const stake = async (mrKrabsContract, pid, amount, account) => {
  if (pid === DefaultEspressoPoolId) {
    return mrKrabsContract.methods
      .enterStaking(new BigNumber(amount).times(new BigNumber(10).pow(18)).toString())
      .send({ from: account })
      .on('transactionHash', (tx) => {
        return tx.transactionHash
      })
  }

  return mrKrabsContract.methods
    .deposit(pid, new BigNumber(amount).times(new BigNumber(10).pow(18)).toString())
    .send({ from: account })
    .on('transactionHash', (tx) => {
      return tx.transactionHash
    })
}

