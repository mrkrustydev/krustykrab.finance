export const harvest = async (mrKrabsContract, pid, account) => {
  if (pid === DefaultEspressoPoolId) {
    return mrKrabsContract.methods
      .leaveStaking(DefaultEspressoPoolId.toString())
      .send({ from: account })
      .on('transactionHash', (tx) => {
        return tx.transactionHash
      })
  }
  return mrKrabsContract.methods
    .deposit(pid, DefaultEspressoPoolId.toString())
    .send({ from: account })
    .on('transactionHash', (tx) => {
      return tx.transactionHash
    })
}

